package de.schuette.cobra2D.ressource;

public class ImageMemoryException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6610823212842396031L;

	public ImageMemoryException() {
	}

	public ImageMemoryException(final String arg0) {
		super(arg0);
	}

	public ImageMemoryException(final Throwable arg0) {
		super(arg0);
	}

	public ImageMemoryException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

}
